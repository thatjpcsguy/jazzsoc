		<h1 class="soul-heading half">The Band</h1>
		<br />
		<p>SOUL (Sounds of University Life) is JazzSoc’s flagship big band. The band is composed entirely of Sydney University students enrolled in a diverse range of degrees, and aims to provide students with opportunities to perform and promote jazz around USYD.</p>
		<p>We play a range of music, from funk through to swing and latin, and incorporate both instrumental and vocal charts in our repertoire. More recently, we have developed an interest in encouraging aspiring jazz composers to submit original compositions and/or arrangements to be played by SOUL. This enables the band to explore fresh material whilst giving amateur composers the chance to have their work performed.</p>
		<p>Some notable performances from 2013 include a joint performance with the Acappelicans and a musical cabaret extravaganza with singers from MUSE. SOUL also performed at the 2013 SULS Ball at Sydney Town Hall, the 2013 Converge festival featuring special guest Nick Bowd on baritone sax, and at City Recital Hall as part of the ‘A Little Lunch Music’ concert series.</p>
		<p>Auditions to join SOUL are held at the start of each year.</p>

